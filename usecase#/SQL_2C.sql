SELECT
CASE WHEN BILLINGSTREET IS NULL or 
BILLINGCITY IS NULL 
OR BILLINGSTATE  IS NULL or BILLINGPOSTALCODE  IS NULL or BILLINGCOUNTRY  IS NULL 
THEN 'Missing Billing Address' ELSE 'Billing Address Present' END AS HOUSEHOLD_ADDR_IND, count(*)
FROM "PROD_CTF_RAW"."SRC_SF"."CTF_SF_ACCOUNT" 
WHERE TYPE='Household'
GROUP BY 1